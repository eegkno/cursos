public class Switch {
        public static void main (String [] args) {
                int a = 2;
                switch(a){
                        case 1:
                                System.out.println("Se eligiO la opciOn 1");
                                break;
                        case 2:
                                System.out.println("Se eligiO la opciOn 2");
                                break;
                        case 3:
                                System.out.println("Se eligiO la opciOn 3");
                                break;
                        default:
                                System.out.println("No se eligiO nada");

                }
        }
}
